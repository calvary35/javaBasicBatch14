package com.syntax.class08;

public class NestedForLoopsReplits {

	public static void main(String[] args) {
		
		 for(int i =1;i<=5;i++){
		      if(i==1){
		        for(int j = 1;j<=10;j++){
		          
		          System.out.print(j + " ");
		        }
		        
		        System.out.println();
		        }
		      if(i==2){
		        for(int k=2; k<=20;k++){
		          if(k%2==0){
		            System.out.print(k + " ");
		          }
		          System.out.println();
		      }
		        if(i==3){
		        for(int l =3; l<=30;l++){
		          System.out.print(l + " ");
		        }
		         
		      }
		        if(i==4){
		          for(int m =4; m<=40;m++){
		            System.out.print(m + " ");
		          }
		          System.out.println();
		        }
		      }
		      }
		}
		}
		       


